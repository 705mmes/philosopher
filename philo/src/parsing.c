#include "../philo.h"

int	check_args(char **s)
{
	int	i;
	int	u;

	i = 0;
	u = -1;
	while (s[++i])
	{
		while (s[i][++u])
		{
			if (!ft_is_digit(s[i][u]))
				return (1);
		}
		u = -1;
	}
	return (0);
}
